#include <AO.h>
int add(int n1, int n2)
{
  int result;
  result = n1 + n2;
  return result;
}
int subtract(int n1, int n2)
{
  int result;
  result = n1 - n2;
  return result;
}
int multiply(int n1, int n2)
{
  int result;
  result = n1 * n2;
  return result;
}
int divide(int n1, int n2)
{
  int result;
  result = n1 / n2;
  return result;
}

